// ─── Handler ────────────────────────────────────────────────────────────────

export async function POST() {
  try {
    // Проверяем, есть ли уже такой системный шаблон
    const existing = await db
      .select({ id: demoTemplates.id, name: demoTemplates.name })
      .from(demoTemplates)
      .where(
        and(
          eq(demoTemplates.name, TEMPLATE_MARKETOLOG_V8.name),
          eq(demoTemplates.isSystem, true),
          isNull(demoTemplates.tenantId),
        )
      )

    if (existing.length > 0) {
      // Обновляем существующий
      await db
        .update(demoTemplates)
        .set({
          sections: TEMPLATE_MARKETOLOG_V8.sections,
          niche: TEMPLATE_MARKETOLOG_V8.niche,
          length: TEMPLATE_MARKETOLOG_V8.length,
          updatedAt: new Date(),
        })
        .where(eq(demoTemplates.id, existing[0].id))

      return NextResponse.json({
        ok: true,
        action: "updated",
        id: existing[0].id,
        name: TEMPLATE_MARKETOLOG_V8.name,
        lessons: TEMPLATE_MARKETOLOG_V8.sections.length,
      })
    }

    // Создаём новый
    const [created] = await db
      .insert(demoTemplates)
      .values({
        tenantId: null,
        name: TEMPLATE_MARKETOLOG_V8.name,
        niche: TEMPLATE_MARKETOLOG_V8.niche,
        length: TEMPLATE_MARKETOLOG_V8.length,
        isSystem: true,
        sections: TEMPLATE_MARKETOLOG_V8.sections,
        audience: ["candidates"],
      })
      .returning({ id: demoTemplates.id })

    return NextResponse.json({
      ok: true,
      action: "created",
      id: created.id,
      name: TEMPLATE_MARKETOLOG_V8.name,
      lessons: TEMPLATE_MARKETOLOG_V8.sections.length,
    })
  } catch (err) {
    console.error("[seed-marketolog-v8] error:", err)
    const msg = err instanceof Error ? err.message : "Unknown error"
    return NextResponse.json({ ok: false, error: msg }, { status: 500 })
  }
}

// GET = alias для POST (чтобы запускать из браузера)
export async function GET() {
  return POST()
}
