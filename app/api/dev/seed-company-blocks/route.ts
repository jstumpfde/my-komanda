// ─── Handler ────────────────────────────────────────────────────────────────

export async function POST() {
  try {
    const results: Array<{ name: string; action: "created" | "updated"; id: string; placeholders: number }> = []

    for (const block of COMPANY_BLOCKS) {
      // Ищем существующий системный блок с таким же niche
      const existing = await db
        .select({ id: demoTemplates.id })
        .from(demoTemplates)
        .where(
          and(
            eq(demoTemplates.name, block.name),
            eq(demoTemplates.isSystem, true),
            isNull(demoTemplates.tenantId),
          )
        )

      if (existing.length > 0) {
        // Обновляем
        await db
          .update(demoTemplates)
          .set({
            sections: block.sections,
            niche: block.niche,
            length: block.length,
            variablesUsed: block.placeholders,
            updatedAt: new Date(),
          })
          .where(eq(demoTemplates.id, existing[0].id))

        results.push({ name: block.name, action: "updated", id: existing[0].id, placeholders: block.placeholders.length })
      } else {
        // Создаём новый
        const [created] = await db
          .insert(demoTemplates)
          .values({
            tenantId: null,
            name: block.name,
            niche: block.niche,
            length: block.length,
            isSystem: true,
            sections: block.sections,
            variablesUsed: block.placeholders,
            audience: ["candidates"],
          })
          .returning({ id: demoTemplates.id })

        results.push({ name: block.name, action: "created", id: created.id, placeholders: block.placeholders.length })
      }
    }

    return NextResponse.json({
      ok: true,
      total: COMPANY_BLOCKS.length,
      results,
    })
  } catch (err) {
    console.error("[seed-company-blocks] error:", err)
    const msg = err instanceof Error ? err.message : "Unknown error"
    return NextResponse.json({ ok: false, error: msg }, { status: 500 })
  }
}

