const ALL_TEMPLATES = [TEMPLATE_B2B_SALES, TEMPLATE_IT_DEV, TEMPLATE_WORKER]

// ─── Handler ────────────────────────────────────────────────────────────────

export async function POST() {
  try {
    await requireCompany()
  } catch (err) {
    if (err instanceof Response) return err
    return NextResponse.json({ error: "Unauthorized" }, { status: 401 })
  }

  const results: Array<{ name: string; action: "inserted" | "exists" }> = []

  for (const tmpl of ALL_TEMPLATES) {
    const existing = await db
      .select({ id: demoTemplates.id })
      .from(demoTemplates)
      .where(and(
        eq(demoTemplates.name, tmpl.name),
        eq(demoTemplates.isSystem, true),
        isNull(demoTemplates.tenantId),
      ))
      .limit(1)

    if (existing.length > 0) {
      results.push({ name: tmpl.name, action: "exists" })
      continue
    }

    await db.insert(demoTemplates).values({
      tenantId: null,
      name: tmpl.name,
      niche: tmpl.niche,
      length: tmpl.length,
      isSystem: true,
      sections: tmpl.sections,
      audience: ["candidates"],
    })
    results.push({ name: tmpl.name, action: "inserted" })
  }

  return NextResponse.json({ ok: true, results })
}
