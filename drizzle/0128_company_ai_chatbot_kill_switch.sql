-- Безопасность #11: Kill switch для AI-чат-бота на уровне всей компании.
-- При ai_chatbot_killed=true процессор НЕ обрабатывает ни одно входящее
-- сообщение (выход на самом первом шаге, до всех других правил).
-- Это аварийный рубильник на случай если что-то пошло не так и нужно
-- мгновенно вырубить AI по всем вакансиям без снятия флага с каждой.

ALTER TABLE companies
  ADD COLUMN IF NOT EXISTS ai_chatbot_killed boolean NOT NULL DEFAULT false;
