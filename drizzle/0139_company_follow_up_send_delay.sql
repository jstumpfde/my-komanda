-- Безопасность отправки follow-up сообщений: per-company минимальная задержка
-- между отправками касаний в hh-чат (в секундах). Меньшие значения повышают
-- риск бана аккаунта hh.ru за подозрительную активность.
--   DEFAULT 31 — рекомендованное значение (совпадает с прежним хардкодом cron'а).
--   Диапазон в UI/API: 21..600. cron берёт значение, защищает нижней границей
--   (если null или < 21 → 31) и умножает на 1000 для миллисекунд.
ALTER TABLE companies
  ADD COLUMN IF NOT EXISTS follow_up_send_delay_seconds integer NOT NULL DEFAULT 31;
