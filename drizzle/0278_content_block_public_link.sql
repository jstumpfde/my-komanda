-- Универсальная (обезличенная) ссылка на контент-блок вакансии (демо/тест).
--
-- Инцидент: HR/партнёр разослал МНОГИМ кандидатам одну ПЕРСОНАЛЬНУЮ ссылку
-- /demo/{token кандидата} — прохождения писались в карточку ОДНОГО кандидата
-- (или short_id-форма уводила в реферальный /visit-редирект, который тоже
-- считает первого визитёра без cookie владельцем ссылки — см.
-- app/api/public/demo/[token]/visit/route.ts). Персональные ссылки нельзя
-- шарить массово.
--
-- public_token — стабильный публичный секрет НА ПАРУ (вакансия, блок) = на
-- строку demos (demos.id уже однозначно определяет и вакансию, и блок).
-- Генерируется ЛЕНИВО при первом запросе «Скопировать общую ссылку» в
-- конструкторе (app/api/modules/hr/demos/[id]/public-link/route.ts), поэтому
-- колонка nullable. Publicный вход — GET/POST /api/public/start/[token] и
-- страница /start/[token]: кандидат вводит имя+телефон, система матчит его
-- ВНУТРИ вакансии по нормализованному телефону (создаёт нового при
-- отсутствии) и редиректит на ЕГО персональный /demo или /test с ?block=.
--
-- unique() допускает множество NULL (демо/тесты без сгенерированной ссылки) —
-- как candidates.short_id.
ALTER TABLE demos ADD COLUMN IF NOT EXISTS public_token text;
CREATE UNIQUE INDEX IF NOT EXISTS demos_public_token_key ON demos (public_token);
