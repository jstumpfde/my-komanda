-- P0-22: editable стоп-слова на уровне вакансии.
-- Раньше список был захардкожен в lib/followup/stop-words.ts (STOP_WORDS).
-- Теперь HR может добавлять/убирать слова в табе «Воронка», и matchStopWordList
-- принимает список из vacancies.stop_words_json вместо модульного default'a.
--
-- Дефолтный набор соответствует историческому STOP_WORDS — миграция применяет
-- его для существующих вакансий чтобы поведение не изменилось до явного
-- ручного редактирования.

ALTER TABLE vacancies
  ADD COLUMN IF NOT EXISTS stop_words_json jsonb NOT NULL DEFAULT
    '["нет","неактуально","не подходит","спасибо","неинтересно","не интересно","не интересует","не актуально","не актуальна","отменяю","отказ","отказываюсь","не рассматриваю"]'::jsonb;
