// Юридический предохранитель (ТК РФ ст. 3, ст. 64; КоАП ст. 5.27, ст. 13.11.1):
// называть кандидату защищённые признаки как причину отказа — НЕЗАКОННО.
// Если текст отказа упоминает возраст/гражданство/национальность/пол/религию/
// прописку — заменяем его на нейтральный, чтобы незаконный текст физически
// не ушёл кандидату. Применяется при планировании и отправке отказа.

const NEUTRAL_REJECTION =
  "К сожалению, по итогам рассмотрения мы продолжим работу с другими кандидатами. Благодарим за интерес к вакансии и желаем удачи в поиске!"

// Фокусные термины с низким риском ложных срабатываний в нейтральном отказе.
const PROTECTED_RE =
  /возраст|молож[ея]|старше\s*\d|гражданств|национальн|вероисповед|религиозн|пропис[кч]|мужчин|женщин/i

export function containsProtectedReason(text?: string | null): boolean {
  return !!text && PROTECTED_RE.test(text)
}

/** Возвращает безопасный текст: если упомянут защищённый признак — нейтральный дефолт. */
export function sanitizeRejectionText(text?: string | null): string | null {
  if (text == null) return null
  if (PROTECTED_RE.test(text)) {
    console.warn("[legal-guard] текст отказа упоминал защищённый признак → заменён на нейтральный")
    return NEUTRAL_REJECTION
  }
  return text
}
