export function getProductBySlug(slug: string): ProductPublicManifest | undefined {
  return PRODUCT_CATALOG.find((p) => p.slug === slug && !p.parentSlug)
}

export function getSatelliteBySlugs(
  parentSlug: string,
  childSlug: string,
): ProductPublicManifest | undefined {
  return PRODUCT_CATALOG.find(
    (p) => p.parentSlug === parentSlug && p.slug === childSlug,
  )
}

export function getSatellites(parentSlug: string): ProductPublicManifest[] {
  return PRODUCT_CATALOG.filter((p) => p.parentSlug === parentSlug)
}

/** Продукты для витрины: только public/beta, без сателлитов (они внутри лендинга флагмана) и без external (своя карточка) */
export function getShowcaseProducts(): ProductPublicManifest[] {
  return PRODUCT_CATALOG.filter(
    (p) => (p.status === "public" || p.status === "beta") && !p.parentSlug,
  )
}

export function getExternalProducts(): ProductPublicManifest[] {
  return PRODUCT_CATALOG.filter((p) => p.status === "external")
}

/** Резолвит manifest по массиву slug'ов из worksWith (умеет "hiring/outbound" сателлиты) */
export function resolveWorksWith(slugs: string[]): ProductPublicManifest[] {
  return slugs
    .map((s) => {
      if (s.includes("/")) {
        const [parent, child] = s.split("/")
        return getSatelliteBySlugs(parent, child)
      }
      return getProductBySlug(s)
    })
    .filter((p): p is ProductPublicManifest => Boolean(p))
}

/** Полный публичный путь продукта, с учётом сателлитов */
export function productHref(p: ProductPublicManifest): string {
  if (p.status === "external" && p.externalUrl) return p.externalUrl
  if (p.parentSlug) return `/products/${p.parentSlug}/${p.slug}`
  return `/products/${p.slug}`
}
