// lib/products/catalog.ts
//
// Единый каталог публичных продуктов Company24 — источник правды для
// /products (витрина-хаб) и /products/[slug] (лендинги), а также для
// будущих футера/sitemap/прайса (docs/architecture/PRODUCT-UMBRELLA-STRATEGY-2026-07.md, §3.4, §9).
//
// Правила заполнения:
//   - status: 'public' — боевой продукт, выводится в витрину без оговорок.
//     'beta' — существует/собирается, но не полностью готов к продаже —
//     показывается с бэджем «Скоро». 'internal' — не выводится в публичную
//     витрину вовсе (правило §9: «витрина не обещает того, что не готово»).
//   - parentSlug — для сателлитов флагмана «Найм» (§10 стратегии): рендерятся
//     тем же шаблоном по вложенному пути /products/hiring/<child>.
//   - Цены НЕ храним и не выдумываем — везде только CTA «Получить демо» → /register.

import type { LucideIcon } from "lucide-react"

export type ProductStatus = "public" | "beta" | "internal" | "external"

export interface ProductFeature {
  title: string
  description: string
}

export interface ProductFaqItem {
  q: string
  a: string
}

export interface ExampleReportKpi {
  label: string
  value: string
}

export interface ExampleReportManagerRow {
  name: string
  calls: number
  /** Средняя оценка звонка, например по 10-балльной шкале */
  avgScore: number
  /** Средний % выполнения чек-листа, 0-100 */
  checklistPercent: number
}

export interface ExampleReport {
  /** Заголовок блока, например «Пример отчёта» */
  title: string
  /** Пояснение, что данные вымышленные/обезличенные */
  disclaimer: string
  kpis: ExampleReportKpi[]
  managers: ExampleReportManagerRow[]
  /** Короткие выводы-инсайты под таблицей */
  insights: string[]
}

export interface ProductPublicManifest {
  slug: string
  /** Только латиница/дефисы, не пересекается со slug'ами других продуктов */
  publicName: string
  tagline: string
  shortDescription: string
  /** Имя иконки из lucide-react (см. ICONS в products/icons.ts) */
  icon: string
  status: ProductStatus
  heroTitle: string
  heroSubtitle: string
  painPoints: string[]
  features: ProductFeature[]
  /** Slugs соседних продуктов для блока «Работает вместе с» */
  worksWith: string[]
  faq: ProductFaqItem[]
  /** Опциональный блок «Пример отчёта» — KPI + таблица менеджеров + выводы (данные примера) */
  exampleReport?: ExampleReport
  /** Для сателлитов флагмана — slug родителя, рендерится /products/[parent]/[slug] */
  parentSlug?: string
  /** Для внешних продуктов (Маркет Радар) — ссылка вместо собственного лендинга */
  externalUrl?: string
}

