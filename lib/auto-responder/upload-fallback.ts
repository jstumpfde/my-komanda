/** Ключевые слова-триггеры («не могу загрузить видео», «не грузится» и т.п.). */
export const DEFAULT_UPLOAD_PROBLEM_KEYWORDS: string[] = [
  "не могу загрузить",
  "не могу загрузиь",
  "не загружается",
  "не грузится",
  "не загрузить видео",
  "не удается загрузить",
  "не удаётся загрузить",
  "видео не отправляется",
  "файл слишком большой",
  "великий файл",
  "не получается загрузить",
  "ошибка загрузки",
]

