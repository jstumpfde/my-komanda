function normalize(text: string): string {
  return text.toLowerCase().replace(/\s+/g, " ").trim()
}

/** true, если текст похож на жалобу «не могу загрузить файл». */
export function matchUploadProblemKeyword(text: string): boolean {
  if (!text) return false
  const norm = normalize(text)
  if (!norm) return false
  return DEFAULT_UPLOAD_PROBLEM_KEYWORDS.some(kw => norm.includes(normalize(kw)))
}
