// Финальный страж исходящих сообщений кандидату (перед отправкой в hh).
// Детерминированная страховка ПОСЛЕ рендера шаблона: чистит то, что не должно
// улететь, и сообщает о проблемах (для лога/алерта). Применяется на самом нижнем
// уровне отправки (lib/hh-api.ts) — через него проходят ВСЕ сообщения: первое,
// дожимы, ответы бота, оффер. Юрий 27.06: «чтобы не улетало некорректное».
//
// Что ловит:
//  - неподставленные переменные {{...}} → вырезает (иначе кандидат видит «{{step_noun}}»);
//  - артефакты после вырезания (двойные пробелы, пустые «», висячее «:» в конце,
//    пробел перед знаком препинания);
//  - кривое приветствие с пустым именем («Здравствуйте, !» / начальная запятая);
//  - пустое сообщение после чистки → флаг empty (отправлять нельзя).
//
// НЕ трогает корректные сообщения (для них это no-op).

export interface GuardResult {
  text:   string    // очищенный текст (отправляем его)
  issues: string[]  // что нашли (для лога; пусто = всё чисто)
  safe:   boolean   // false → текст пустой/битый, отправлять НЕ стоит
}

export function guardOutgoingMessage(raw: string | null | undefined): GuardResult {
  const issues: string[] = []
  let t = (raw ?? "").toString()

  // 1) Неподставленные плейсхолдеры {{...}} — вырезаем, фиксируем какие.
  const leftover = t.match(/\{\{\s*[^}]*?\s*\}\}/g)
  if (leftover && leftover.length > 0) {
    issues.push("unresolved_placeholders: " + Array.from(new Set(leftover)).join(", "))
    t = t.replace(/\{\{\s*[^}]*?\s*\}\}/g, "")
  }

  // 2) Кривое приветствие с пустым именем.
  const before2 = t
  t = t
    .replace(/^[\s,]+/, "")                                   // ведущие пробелы/запятые («, привет» от пустого имени)
    // «Здравствуйте, !» → «Здравствуйте!». Без \b — в JS \b не работает с кириллицей (ASCII-only).
    .replace(/(Здравствуйте|Приветствую|Привет|Добрый день|Доброе утро|Добрый вечер),\s*([!?.])/gi, "$1$2")
  if (t !== before2) issues.push("fixed_empty_name_greeting")

  // 3) Артефакты вырезания: пустые «», висячие скобки/пробелы/пунктуация.
  t = t
    .replace(/«\s*»/g, "")            // пустые «ёлочки» (если вырезали {{vacancy}})
    .replace(/"\s*"/g, "")            // пустые кавычки
    .replace(/\(\s*\)/g, "")          // пустые скобки
    .replace(/([(«„"])\s+/g, "$1")    // пробел после открывающей
    .replace(/\s+([)»“",.;:!?])/g, "$1") // пробел перед закрывающей/пунктуацией
    .replace(/[ \t]{2,}/g, " ")       // двойные пробелы
    .replace(/[ \t]+\n/g, "\n")       // пробелы в конце строк
    .replace(/\n{3,}/g, "\n\n")       // лишние переводы строк
    .replace(/[ \t]*[:\-—–][ \t]*$/g, "") // висячее «:»/«—» в конце (напр. «посмотрите:» без ссылки)
    .trim()

  // 4) Пустое после чистки — отправлять нельзя.
  const safe = t.trim().length > 0
  if (!safe) issues.push("empty_after_clean")

  return { text: t, issues, safe }
}
