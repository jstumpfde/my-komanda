// Множество для O(1)-поиска, уже нормализованное.
const NAME_SET = new Set(NAMES)

/** Нормализация токена под сравнение со словарём: trim → lowercase → ё→е. */
export function normalizeNameToken(s: string): string {
  return s.trim().toLowerCase().replace(/ё/g, "е")
}

/** Известное ли это имя? Пустые/много-словные строки обрезаются до первого слова. */
export function isKnownGivenName(token: string | null | undefined): boolean {
  if (!token) return false
  const first = token.trim().split(/\s+/)[0] ?? ""
  if (!first) return false
  return NAME_SET.has(normalizeNameToken(first))
}
