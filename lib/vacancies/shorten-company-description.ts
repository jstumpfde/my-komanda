// Описание компании для блока «О компании» в вакансии.
// Тянем часть «о компании» ДОСЛОВНО (один-в-один, без сжатия/перефразирования) —
// убираем только то, что НЕ про компанию: условия, «кого мы ищем» (требования),
// «Что мы даём», буллет-списки. Решение Юрия: не ужимать текст, резать только
// целыми абзацами; потолок — 1000 символов. Настройки компании не меняются —
// усечение только при подтягивании в вакансию.

// Маркеры начала секций, которым НЕ место в блоке «О компании» вакансии.
const STOP_SECTION = /^(что мы даём|что мы предлагаем|мы предлагаем|что вы получ|что ты получ|ищем человек|ищем того|кого ищем|кого мы ищем|требовани|обязанност|условия|задачи|чем предстоит|что нужно|от вас|ваши задачи|мы ждём|мы ищем)/i

const MAX_CHARS = 1000

export function shortenCompanyDescription(text: string | null | undefined): string {
  if (!text) return ""
  const paras = text.split(/\n\s*\n/).map((p) => p.trim()).filter(Boolean)

  // Берём вводные абзацы про компанию ДОСЛОВНО — до первой секции условий/
  // требований или абзаца-перечисления (≥2 буллета).
  const kept: string[] = []
  for (const p of paras) {
    if (STOP_SECTION.test(p)) break
    const bullets = p.split("\n").filter((l) => /^\s*[—\-•*·]/.test(l)).length
    if (bullets >= 2) break
    kept.push(p)
  }

  // Потолок 1000 симв — отрезаем ЦЕЛЫЕ хвостовые абзацы, текст не режем/не сжимаем.
  let result = kept.length ? kept : paras.slice(0, 1)
  let out = result.join("\n\n").trim()
  while (out.length > MAX_CHARS && result.length > 1) {
    result = result.slice(0, -1)
    out = result.join("\n\n").trim()
  }
  return out
}
