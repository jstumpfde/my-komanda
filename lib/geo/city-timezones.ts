/**
 * Извлекает регион в скобках из строки вида «Внуковское (Московская область)».
 * Возвращает null, если скобок нет.
 */
function extractParenRegion(input: string): string | null {
  const m = /\(([^)]+)\)/.exec(input)
  return m ? m[1].trim() : null
}

/** Убирает «г.», «г », «пос.», «с.» и т.п. частые префиксы российских городов. */
function stripCityPrefixes(input: string): string {
  return input
    .replace(/^г\.\s*/i, "")
    .replace(/^гор\.\s*/i, "")
    .replace(/^пос\.\s*/i, "")
    .replace(/^с\.\s*/i, "")
    .trim()
}

function normalize(input: string): string {
  return stripCityPrefixes(input.trim()).toLowerCase()
}

/** Точное совпадение по ключу справочника (после нормализации). */
function exactLookup(normalized: string): number | null {
  if (!normalized) return null
  const hit = CITY_UTC_OFFSETS[normalized]
  return typeof hit === "number" ? hit : null
}

/** Частичное совпадение по вхождению — ключ справочника содержится в строке
 *  или наоборот (для составных названий вроде «г. Москва, м. Юго-Западная»). */
function partialLookup(normalized: string): number | null {
  if (!normalized) return null
  for (const [key, offset] of Object.entries(CITY_UTC_OFFSETS)) {
    if (normalized.includes(key) || key.includes(normalized)) return offset
  }
  return null
}

/**
 * Резолвит UTC-оффсет (часы) по строке города/региона из резюме hh.
 *
 * Поддерживает:
 *  - точные и частичные совпадения по городу;
 *  - формат «Город (Область)» — пробует город, затем область в скобках;
 *  - неизвестный город → null (fail-open — штраф не применяется).
 */
export function resolveCityUtcOffset(city: string | null | undefined): number | null {
  if (!city || typeof city !== "string") return null
  const trimmed = city.trim()
  if (!trimmed) return null

  const region = extractParenRegion(trimmed)
  // Часть строки до скобок — «сам город».
  const cityPart = trimmed.replace(/\([^)]*\)/, "").trim()

  const cityNorm = normalize(cityPart)
  const exactCity = exactLookup(cityNorm)
  if (exactCity != null) return exactCity

  if (region) {
    const regionNorm = normalize(region)
    const exactRegion = exactLookup(regionNorm)
    if (exactRegion != null) return exactRegion
  }

  const partialCity = partialLookup(cityNorm)
  if (partialCity != null) return partialCity

  if (region) {
    const regionNorm = normalize(region)
    const partialRegion = partialLookup(regionNorm)
    if (partialRegion != null) return partialRegion
  }

  // Последняя попытка — вся исходная строка целиком (без разбора скобок).
  const wholeNorm = normalize(trimmed)
  const wholeExact = exactLookup(wholeNorm)
  if (wholeExact != null) return wholeExact
  const wholePartial = partialLookup(wholeNorm)
  if (wholePartial != null) return wholePartial

  return null
}
