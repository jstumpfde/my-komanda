/** Несклоняемые в русской речи топонимы — возвращаем как есть (ключ — нижний
 *  регистр для поиска, значение — правильное написание). */
const INDECLINABLE: Record<string, string> = {
  "сочи": "Сочи",
  "гродно": "Гродно",
  "тольятти": "Тольятти",
  "баку": "Баку",
  "тбилиси": "Тбилиси",
  "сухуми": "Сухуми",
  "батуми": "Батуми",
  "поти": "Поти",
  "улан-удэ": "Улан-Удэ",
}

const HARD_CONSONANTS = /[бвгджзклмнпрстфхцчшщ]$/i

/**
 * Общие морфологические правила для однословных названий (нет в словаре и
 * не несклоняемое). Возвращает null, если ни одно правило не подходит
 * уверенно — тогда вызывающий код должен использовать safe fallback.
 */
function applyHeuristics(city: string): string | null {
  // Составные названия (через пробел) — не гадаем, слишком велик риск
  // склонить только часть словосочетания.
  if (/\s/.test(city)) return null

  if (/ия$/i.test(city)) return city.replace(/ия$/i, "ии")
  if (/ь$/i.test(city)) return city.replace(/ь$/i, "и")
  if (/й$/i.test(city)) return city.replace(/й$/i, "е")
  if (/[ая]$/i.test(city)) return city.replace(/[ая]$/i, "е")
  if (HARD_CONSONANTS.test(city)) return `${city}е`

  // о/е/и/у/ы-финали и всё прочее — слишком неоднозначно (Иваново/Кемерово
  // формально склоняются, но обиходно всё чаще нет; Сочи/Чебоксары и т.п.
  // разбираются словарём/множеством несклоняемых выше) — не гадаем.
  return null
}

/**
 * Город в предложном падеже, готовый к подстановке после предлога «в».
 *
 * `cityPrepositionalRu("Москва")`      → "Москве"     → «в Москве»
 * `cityPrepositionalRu("Сочи")`        → "Сочи"       → «в Сочи»
 * `cityPrepositionalRu("Верхние Мандрики")` → "городе Верхние Мандрики"
 *                                       → «в городе Верхние Мандрики»
 * `cityPrepositionalRu(null)` / `""`   → ""
 */
export function cityPrepositionalRu(cityRaw: string | null | undefined): string {
  const city = (cityRaw ?? "").trim()
  if (!city) return ""

  const key = city.toLowerCase()

  const dictHit = CITY_PREPOSITIONAL[key]
  if (dictHit) return dictHit

  const indeclinableHit = INDECLINABLE[key]
  if (indeclinableHit) return indeclinableHit

  const heuristic = applyHeuristics(city)
  if (heuristic) return heuristic

  return `городе ${city}`
}

/**
 * Готовая фраза «в …» для лендинга/сообщений (обёртка над
 * {@link cityPrepositionalRu}, включает сам предлог и защиту от пустого
 * города — используй, когда нужна фраза целиком, а не только форма города).
 *
 * `inCityRu("Москва")` → "в Москве"
 * `inCityRu(null)`     → ""
 */
export function inCityRu(cityRaw: string | null | undefined): string {
  const declined = cityPrepositionalRu(cityRaw)
  return declined ? `в ${declined}` : ""
}
