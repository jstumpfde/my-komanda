// Склонение русского топонима (города) в предложный падеж («в …») — для
// текстов лендинга вакансии и подобных мест, где город подставляется в
// готовую фразу. СПРАВОЧНИК ниже — лингвистический факт (как
// lib/geo/city-timezones.ts, lib/plural-ru.ts), не «хардкод контента»,
// который нельзя трогать без задачи: грамматика русского языка не является
// настраиваемым бизнес-контентом.
//
// Баг-контекст: components/vacancies/publish-tab.tsx генерировал
// «Современный офис в Москва» (город в именительном падеже вместо
// предложного) — см. память landing-publish-tab-backlog.md, п.1.
//
// Порядок разрешения:
//   1. Точный словарь ~50 крупных и/или грамматически неочевидных городов РФ
//      (в т.ч. составные названия — «Нижний Новгород», «Ростов-на-Дону») —
//      готовая форма руками, не полагаемся на эвристику даже там, где она
//      случайно угадала бы верно.
//   2. Несклоняемые (Сочи, Гродно, Тольятти, Баку, Тбилиси…) — возвращаем
//      город как есть.
//   3. Общие морфологические правила для однословных названий, которых нет
//      в словаре:
//        ...ия → ...ии   (Мордовия → Мордовии)
//        ...ь  → ...и    (по умолчанию — женский род 3-го склонения)
//        ...й  → ...е    (Валдай → Валдае)
//        ...а/...я → ...е (Анапа → Анапе)
//        согласная на конце → + «е» (самый частый случай — 2-е склонение
//        мужского рода: Брянск → Брянске)
//      К составным названиям (через пробел) правила НЕ применяются — риск
//      «полусклонения» одной из частей («Старый Оскол» → «Старый Осколе»
//      вместо «Старом Осколе»); для них сразу используется safe fallback.
//   4. Город не распознан — безопасный fallback «городе {Имя}»: грамматически
//      корректная фраза («в городе Верхние Мандрики») без риска гадать
//      неправильную форму.
//
// Использование: `в ${cityPrepositionalRu(city)}` — функция уже возвращает
// то, что идёт ПОСЛЕ предлога «в» (включая случай безопасной fallback-обёртки).

