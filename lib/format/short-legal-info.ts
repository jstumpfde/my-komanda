/**
 * Display-only сокращение полного юр. наименования и юр. адреса для
 * админ-карточки клиента (components/admin/clients/company-sheet.tsx).
 *
 * НЕ трогает то, что реально хранится в companies.fullName/legalAddress —
 * эти поля используются для реальных счетов/актов (billing-documents.ts) и
 * обязаны оставаться полными и юридически точными. Здесь — только косметика
 * вывода в карточке (Юрий 08.07: длинные DaData-формулировки мешают читать).
 */

// "Индивидуальный предприниматель Иванов Иван Иванович" → "ИП Иванов И. И."
// Названо shortLegalName (не shortCompanyName) — в company-sheet.tsx уже есть
// одноимённая локальная функция с другим смыслом (short-имя для шапки шита).
export function shortLegalName(full: string | null | undefined): string | null {
  if (!full) return full ?? null
  const m = full.match(/^Индивидуальный предприниматель\s+(.+)$/i)
  if (!m) return full
  const parts = m[1].trim().split(/\s+/)
  if (parts.length >= 3) {
    const [surname, first, patronymic] = parts
    return `ИП ${surname} ${first[0]}.${patronymic[0]}.`
  }
  if (parts.length === 2) return `ИП ${parts[0]} ${parts[1][0]}.`
  return `ИП ${m[1]}`
}

// Убирает почтовый индекс и канцелярские формулировки DaData
// ("Город N столица Российской Федерации город федерального значения",
// "N-ый административный округ", "район N") — индекс и город уже есть
// отдельными полями в той же карточке, повторять их длинно не нужно.
export function shortLegalAddress(full: string | null | undefined): string | null {
  if (!full) return full ?? null
  let s = full

  // Ведущий 6-значный индекс с запятой.
  s = s.replace(/^\d{6},\s*/, "")

  // "Город Москва столица Российской Федерации город федерального значения," → "г. Москва, РФ,"
  s = s.replace(
    /Город\s+([А-ЯЁа-яё-]+)\s+столица\s+Российской\s+Федерации\s+город\s+федерального\s+значения,?\s*/i,
    "г. $1, РФ, ",
  )

  // Остаточное "Российская Федерация"/"Российской Федерации" → "РФ".
  s = s.replace(/Российской Федерации|Российская Федерация/gi, "РФ")

  // "Северо-Западный административный округ" → "Северо-Западный АО".
  s = s.replace(/([А-ЯЁа-яё-]+)\s+административный округ/gi, "$1 АО")

  // "район Хорошево-Мневники" → "р. Хорошево-Мневники". Без \b — в JS оно
  // ASCII-based (не работает на границе с кириллицей: "р" не входит в \w).
  s = s.replace(/район\s+([А-ЯЁа-яё0-9\- ]+?)(,|$)/gi, "р. $1$2")

  // Схлопнуть пробелы вокруг запятых и убрать хвостовую запятую.
  s = s.replace(/\s*,\s*/g, ", ").replace(/,\s*,/g, ",").replace(/,\s*$/, "").trim()

  return s
}
