// Группа 25 (доп.): AI-«Актуализация» Портрета под изменившуюся вакансию.
// В отличие от suggest-requirements (генерация С НУЛЯ) — здесь на входе ТЕКУЩИЕ
// критерии Портрета + актуальное описание вакансии, на выходе ДИФФ:
//   add           — чего НЕТ в текущих, но стоит добавить под новую вакансию
//   maybe_outdated — текущие пункты, которые могли устареть/перестать подходить
// Используется в /api/modules/hr/vacancies/[id]/requirements/actualize.
// model: AI_MODEL_MAIN (см. lib/ai/models.ts). temperature убран — Sonnet 5 не принимает.

export interface ActualizeRequirementsPromptInput {
  vacancyTitle:       string
  vacancyIndustry:    string | null
  vacancyDescription: string
  /** Текущие пункты «Подходит» (что хотим видеть) — тексты критериев. */
  currentGood:        string[]
  /** Текущие пункты «Не подходит по смыслу» — тексты дисквалификаторов. */
  currentBad:         string[]
}

export function buildActualizeRequirementsPrompt(input: ActualizeRequirementsPromptInput): string {
  const { vacancyTitle, vacancyIndustry, vacancyDescription, currentGood, currentBad } = input
  return `Ты помощник HR. У вакансии уже есть «Портрет кандидата» (критерии отбора).
Вакансию обновили — описание ниже актуальное. Твоя задача — НЕ переписывать всё с нуля,
а аккуратно ПРЕДЛОЖИТЬ изменения: что ДОБАВИТЬ и что, возможно, УСТАРЕЛО.

ВАКАНСИЯ: ${vacancyTitle}
ИНДУСТРИЯ: ${vacancyIndustry ?? "не указана"}

АКТУАЛЬНОЕ ОПИСАНИЕ:
${vacancyDescription}

ТЕКУЩИЕ КРИТЕРИИ «ПОДХОДИТ» (что хотим видеть):
${currentGood.length ? currentGood.map(t => `- ${t}`).join("\n") : "(пусто)"}

ТЕКУЩИЕ КРИТЕРИИ «НЕ ПОДХОДИТ» (дисквалификаторы):
${currentBad.length ? currentBad.map(t => `- ${t}`).join("\n") : "(пусто)"}

Верни JSON строго по схеме (без markdown-блоков, без префиксов):
{
  "add_good":        ["..."],
  "add_bad":         ["..."],
  "maybe_outdated_good": ["..."],
  "maybe_outdated_bad":  ["..."]
}

ГЛАВНЫЙ ПРИНЦИП: МИНИМУМ изменений, по СУТИ. Большинство текущих критериев скорее всего
остаются. Предлагай добавить ТОЛЬКО то, чего реально не хватает под новое описание.

ПРАВИЛА:
- add_good / add_bad — НОВЫЕ критерии, которых НЕТ среди текущих (даже близко по смыслу),
  но которые следуют из обновлённого описания. Если ничего добавлять не нужно — верни [].
  0-3 пункта на список. Формулируй ШИРОКО, по сути (как один критерий, синонимы — через
  запятую внутри пункта). НЕ дублируй то, что уже есть в текущих списках.
- maybe_outdated_good / maybe_outdated_bad — пункты ИЗ ТЕКУЩИХ списков (дословно, как
  даны выше), которые под новое описание выглядят устаревшими / больше не релевантными /
  противоречащими. Это НЕ приказ удалить — HR проверит сам. Если всё актуально — верни [].
  Бери только то, что реально вызывает сомнение; не сомневайся в каждом пункте.
- Не дроби навыки на под-виды. Не предлагай общих фраз вроде «опытный специалист».
- Стоп-факторы по формальным признакам (город/возраст/формат) НЕ трогай — они отдельно.

ВАЖНО: возвращай ТОЛЬКО валидный JSON, без комментариев.`
}
