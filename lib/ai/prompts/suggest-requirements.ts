// Группа 25: AI-предложение структурированных требований из описания вакансии.
// Используется в /api/modules/hr/vacancies/[id]/requirements/suggest.
// model: AI_MODEL_MAIN (см. lib/ai/models.ts). temperature убран — Sonnet 5 не принимает.
//
// Эталонный дефолт (07.07.2026, Юрий): результат ложится в «Портрет» осевым
// скорингом (scoringMode="axes") — у каждого направления nice_to_have есть
// weight (баллы оси, Σ по всем ≈100, более важные направления — больше).
// См. applySuggestion() в components/vacancies/spec-editor.tsx и buildAxes()
// в lib/core/spec/axis-scorer.ts.

export interface SuggestRequirementsPromptInput {
  vacancyTitle:    string
  vacancyIndustry: string | null
  vacancyDescription: string
}

export function buildSuggestRequirementsPrompt(input: SuggestRequirementsPromptInput): string {
  const { vacancyTitle, vacancyIndustry, vacancyDescription } = input
  return `Ты помощник HR. Прочитай описание вакансии и выдели структурированные требования к кандидатам.

ВАКАНСИЯ: ${vacancyTitle}
ИНДУСТРИЯ: ${vacancyIndustry ?? "не указана"}

ОПИСАНИЕ:
${vacancyDescription}

Верни JSON строго по схеме (без markdown-блоков, без префиксов):
{
  "must_have":     ["..."],
  "nice_to_have":  [{ "text": "...", "weight": N }],
  "deal_breakers": ["..."],
  "ideal_profile": "..."
}

ГЛАВНЫЙ ПРИНЦИП: НЕ плоский список гранулярных навыков, а 3-5 СГРУППИРОВАННЫХ
НАПРАВЛЕНИЙ. Каждое направление = одна область компетенций, внутри неё —
смежные навыки. Не дроби каждый навык в отдельный пункт. Каждое направление —
это отдельная ОСЬ оценки: AI-скоринг проверит её изолированно по резюме и
даст балл именно по этой оси (осевой режим «Портрета»).

ФОРМАТ ПУНКТА text (КРИТИЧЕСКИ ВАЖНО для отрисовки в UI и для скоринга):
"Заголовок направления, навык1, навык2, навык3"
— ПЕРВОЕ слово/фраза до первой запятой = заголовок направления (короткий, 1-3 слова),
  далее через запятую — смежные навыки/синонимы ЭТОГО направления (они станут
  чипами и совпадением по смыслу для скоринга).
Примеры для роли «ассистент маркетолога»:
  { "text": "Тексты, SEO-статьи, посты, упаковка продукта, email-рассылки, лендинги", "weight": 25 }
  { "text": "Визуал, картинки, базовый дизайн, AI-генерация изображений", "weight": 20 }
  { "text": "Видео, монтаж роликов, нарезки, субтитры", "weight": 15 }
  { "text": "СММ и продвижение, Telegram, Max, ведение каналов, контент-план", "weight": 20 }
  { "text": "Продажи, работа с возражениями, общение с клиентами", "weight": 20 }
Пример для «менеджер продаж»:
  { "text": "Продажи, B2B, проектные, тендерные, госзакупки", "weight": 40 }
  { "text": "Переговоры, работа с возражениями, ведение сделки до оплаты", "weight": 35 }
  { "text": "Подтверждённый результат, выполнение плана, цифры по выручке", "weight": 25 }

ВЕСА (weight, поле у каждого nice_to_have):
- Целое число 5-60. Сумма весов ВСЕХ направлений nice_to_have = 100 (допустима
  небольшая погрешность ±2 из-за округления, но стремись к ровно 100).
  Пример распределения на 4 направления: не 25/25/25/25 бездумно, а по факту
  важности для роли — напр. 35/25/25/15.
- Направление, которое СИЛЬНЕЕ ВСЕГО определяет успех в роли (ключевая
  профессиональная компетенция) — самый большой вес. Второстепенные/приятные
  бонусы (напр. "английский язык" для роли без международных клиентов) — вес
  поменьше (5-15).
- НЕ раздавай равные веса по умолчанию — распредели осмысленно по значимости
  для ИМЕННО этой роли, судя по описанию вакансии.

AI-ИНСТРУМЕНТЫ (если роль подразумевает работу с нейросетями — маркетинг,
контент, дизайн, аналитика, разработка, поддержка и т.п.): добавь ОТДЕЛЬНОЕ
направление про это с КАТЕГОРИЙНОЙ оговоркой в тексте — засчитывать любой
уверенный опыт работы с нейросетями, даже если конкретный инструмент в резюме
не назван. Актуальные на 2026 год инструменты для примеров (используй релевантные
роли, не все сразу):
  текст/чат: Claude, ChatGPT, Gemini
  изображения: GPT Image, Midjourney, Nano Banana, Flux, Stable Diffusion
  видео: Veo, Sora, Kling, Seedance, Runway
Формат пункта: "AI-инструменты, любой уверенный опыт с нейросетями (Claude,
ChatGPT, Midjourney и т.п. — даже если инструмент не назван явно)".

ПРАВИЛА:
- 3-5 направлений в nice_to_have. must_have ОСТАВЬ ПУСТЫМ (отсев — это deal_breakers,
  а «что хотим видеть» — только плюс к баллу).
- Заголовок направления — короткий и понятный (Тексты / Визуал / Видео / Продажи).
- Внутри направления — 2-6 смежных навыков, релевантных именно ЭТОЙ вакансии
  (бери из описания, не выдумывай лишнего). Близкие формулировки = синонимы (логика ИЛИ).
- Длина одного пункта ≤ 180 символов (вместе с навыками). Не превышай.
- НЕ дроби одно направление на несколько пунктов. ❌ ПЛОХО: "SEO-статьи", "посты",
  "email" тремя отдельными пунктами. ✅ ХОРОШО: одно "Тексты, SEO-статьи, посты, email".
- НЕ создавай два направления, дублирующих друг друга по смыслу разными словами.
  ❌ ПЛОХО: "Работа с ЛПР, переговоры" и отдельно "Взаимодействие с лицами,
  принимающими решения, презентации" — это ОДНО и то же направление, объедини
  в один пункт. Перед финальным ответом проверь весь список nice_to_have и
  deal_breakers на смысловые дубли — оставь один пункт на каждую уникальную идею.
- deal_breakers — 3-5 что точно НЕ подходит. Тоже можно группировать
  ("Только B2C/розница без B2B-опыта", "Нет опыта в продажах вообще").
  В deal_breakers заголовок-направление НЕ нужен — это цельные фразы.
- ideal_profile — 1-2 предложения, кто реально нужен (≤ 500 символов).
- НЕ дублируй stop-factors (город/возраст/формат — отдельно). Без общих фраз "опытный специалист".
- Всего пунктов в любом списке ≤ 10.

ВАЖНО: возвращай ТОЛЬКО валидный JSON, без комментариев.`
}
