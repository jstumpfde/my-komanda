// Группа 25: AI-предложение структурированных требований из описания вакансии.
// Используется в /api/modules/hr/vacancies/[id]/requirements/suggest.
// model: claude-sonnet-4-6, temperature 0.3 (немного креатива).

export interface SuggestRequirementsPromptInput {
  vacancyTitle:    string
  vacancyIndustry: string | null
  vacancyDescription: string
}

export function buildSuggestRequirementsPrompt(input: SuggestRequirementsPromptInput): string {
  const { vacancyTitle, vacancyIndustry, vacancyDescription } = input
  return `Ты помощник HR. Прочитай описание вакансии и выдели структурированные требования к кандидатам.

ВАКАНСИЯ: ${vacancyTitle}
ИНДУСТРИЯ: ${vacancyIndustry ?? "не указана"}

ОПИСАНИЕ:
${vacancyDescription}

Верни JSON строго по схеме (без markdown-блоков, без префиксов):
{
  "must_have":     ["..."],
  "nice_to_have":  ["..."],
  "deal_breakers": ["..."],
  "ideal_profile": "..."
}

ГЛАВНЫЙ ПРИНЦИП: МАЛО и ШИРОКО, по СУТИ. НЕ дроби и НЕ перечисляй все навыки из
описания. Дай 3-4 КЛЮЧЕВЫХ критерия — то, по чему реально решаем «подходит / нет».

ПРАВИЛА:
- ОБЪЕДИНЯЙ однотипное в ОДИН критерий. Близкие формулировки — синонимами через
  запятую ВНУТРИ одного критерия (засчитается любая, логика ИЛИ).
  ❌ ПЛОХО (дробление на под-виды): "B2B-продажи", "B2G-продажи", "госзакупки",
     "тендерные продажи", "прямые продажи" — это пять критериев про ОДНО и то же.
  ✅ ХОРОШО (один широкий): "Опыт продаж (B2B, проектные, тендерные, госзакупки — любое)".
- Думай о СУТИ роли — отвечай критериями на вопросы:
  1) Умеет ли делать ГЛАВНОЕ в этой работе? (продавец — продавать; маркетолог — растить лиды/трафик)
  2) Есть ли ЭКСПЕРТИЗА в нужном продукте / индустрии / оборудовании?
  3) Есть ли РУКОВОДЯЩИЙ опыт — ЕСЛИ роль руководителя (управлял командой/отделом)?
  4) Есть ли ПОДТВЕРЖДЁННЫЙ результат (цифры)?
  Каждый критерий = одна такая компетенция, НЕ под-вариант.
- must_have — 3-4 таких ШИРОКИХ критерия (не больше). nice_to_have — 0-1, только если
  правда важно (можно пусто).
- deal_breakers — до 2 что точно НЕ подходит ("только B2C/розница без B2B",
  "нет управленческого опыта" если он нужен).
- ideal_profile — 1-2 предложения, кто реально нужен (≤ 500 символов).
- НЕ дублируй stop-factors (город/возраст/формат — отдельно). Без общих фраз "опытный специалист".

ВАЖНО: возвращай ТОЛЬКО валидный JSON, без комментариев.`
}
