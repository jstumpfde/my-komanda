// Группа 25: AI-предложение структурированных требований из описания вакансии.
// Используется в /api/modules/hr/vacancies/[id]/requirements/suggest.
// model: claude-sonnet-4-6, temperature 0.3 (немного креатива).

export interface SuggestRequirementsPromptInput {
  vacancyTitle:    string
  vacancyIndustry: string | null
  vacancyDescription: string
}

export function buildSuggestRequirementsPrompt(input: SuggestRequirementsPromptInput): string {
  const { vacancyTitle, vacancyIndustry, vacancyDescription } = input
  return `Ты помощник HR. Прочитай описание вакансии и выдели структурированные требования к кандидатам.

ВАКАНСИЯ: ${vacancyTitle}
ИНДУСТРИЯ: ${vacancyIndustry ?? "не указана"}

ОПИСАНИЕ:
${vacancyDescription}

Верни JSON строго по схеме (без markdown-блоков, без префиксов):
{
  "must_have":     ["..."],
  "nice_to_have":  ["..."],
  "deal_breakers": ["..."],
  "ideal_profile": "..."
}

ГЛАВНЫЙ ПРИНЦИП: НЕ плоский список гранулярных навыков, а 3-5 СГРУППИРОВАННЫХ
НАПРАВЛЕНИЙ. Каждое направление = одна область компетенций, внутри неё —
смежные навыки. Не дроби каждый навык в отдельный пункт.

ФОРМАТ ПУНКТА (КРИТИЧЕСКИ ВАЖНО для отрисовки в UI):
"Заголовок направления, навык1, навык2, навык3"
— ПЕРВОЕ слово/фраза до первой запятой = заголовок направления (короткий, 1-3 слова),
  далее через запятую — смежные навыки этого направления (они станут чипами).
Примеры для роли «ассистент маркетолога»:
  "Тексты, SEO-статьи, посты, упаковка продукта, email-рассылки, лендинги"
  "Визуал, картинки, базовый дизайн, AI-генерация изображений"
  "Видео, монтаж роликов, нарезки, субтитры"
  "СММ и продвижение, Telegram, Max, ведение каналов, контент-план"
  "Продажи, работа с возражениями, общение с клиентами"
Пример для «менеджер продаж»:
  "Продажи, B2B, проектные, тендерные, госзакупки"
  "Переговоры, работа с возражениями, ведение сделки до оплаты"
  "Подтверждённый результат, выполнение плана, цифры по выручке"

ПРАВИЛА:
- 3-5 направлений в nice_to_have. must_have ОСТАВЬ ПУСТЫМ (отсев — это deal_breakers,
  а «что хотим видеть» — только плюс к баллу).
- Заголовок направления — короткий и понятный (Тексты / Визуал / Видео / Продажи).
- Внутри направления — 2-6 смежных навыков, релевантных именно ЭТОЙ вакансии
  (бери из описания, не выдумывай лишнего). Близкие формулировки = синонимы (логика ИЛИ).
- Длина одного пункта ≤ 180 символов (вместе с навыками). Не превышай.
- НЕ дроби одно направление на несколько пунктов. ❌ ПЛОХО: "SEO-статьи", "посты",
  "email" тремя отдельными пунктами. ✅ ХОРОШО: одно "Тексты, SEO-статьи, посты, email".
- deal_breakers — 3-5 что точно НЕ подходит. Тоже можно группировать
  ("Только B2C/розница без B2B-опыта", "Нет опыта в продажах вообще").
  В deal_breakers заголовок-направление НЕ нужен — это цельные фразы.
- ideal_profile — 1-2 предложения, кто реально нужен (≤ 500 символов).
- НЕ дублируй stop-factors (город/возраст/формат — отдельно). Без общих фраз "опытный специалист".
- Всего пунктов в любом списке ≤ 10.

ВАЖНО: возвращай ТОЛЬКО валидный JSON, без комментариев.`
}
