// Дефолтный шаблон политики конфиденциальности по ФЗ-152.
// Генерируется на основе реквизитов компании. Текст обобщённый, не копия чужих
// политик; компания может отредактировать его в /settings/legal.

export interface CompanyForPolicy {
  name: string
  inn: string
  legalAddress?: string | null
  email: string
}

function fmtDate(d: Date = new Date()): string {
  return d.toLocaleDateString("ru-RU", { day: "2-digit", month: "long", year: "numeric" })
}

export function generateDefaultPrivacyPolicy(company: CompanyForPolicy): string {
  const operator = `${company.name} (ИНН ${company.inn}${company.legalAddress ? `, юридический адрес: ${company.legalAddress}` : ""})`
  const contactEmail = company.email
  const today = fmtDate()

  return [
    `<h1>Политика конфиденциальности</h1>`,
    `<p class="lead">Дата вступления в силу: ${today}</p>`,

    `<h2>1. Общие положения</h2>`,
    `<p>Настоящая Политика конфиденциальности (далее — «Политика») определяет порядок обработки и защиты персональных данных физических лиц (далее — «Субъекты ПД») в ${operator} (далее — «Оператор»).</p>`,
    `<p>Политика разработана в соответствии с Федеральным законом от 27.07.2006 № 152-ФЗ «О персональных данных» и иными нормативными правовыми актами Российской Федерации в области персональных данных.</p>`,
    `<p>Используя сайт и сервисы Оператора, Субъект ПД даёт согласие на обработку своих персональных данных на условиях настоящей Политики.</p>`,

    `<h2>2. Состав обрабатываемых персональных данных</h2>`,
    `<p>Оператор обрабатывает следующие категории персональных данных:</p>`,
    `<ul>`,
    `<li>фамилия, имя, отчество;</li>`,
    `<li>контактные данные (адрес электронной почты, номер телефона);</li>`,
    `<li>сведения о должности, опыте работы и образовании (для соискателей вакансий);</li>`,
    `<li>сведения, содержащиеся в резюме и сопроводительных письмах;</li>`,
    `<li>идентификаторы технических устройств и cookie-файлы (IP-адрес, тип браузера, операционная система);</li>`,
    `<li>иные сведения, предоставленные Субъектом ПД добровольно.</li>`,
    `</ul>`,

    `<h2>3. Цели обработки персональных данных</h2>`,
    `<p>Оператор обрабатывает персональные данные в следующих целях:</p>`,
    `<ul>`,
    `<li>исполнение договорных обязательств перед Субъектом ПД;</li>`,
    `<li>рассмотрение откликов на вакансии и проведение отбора кандидатов;</li>`,
    `<li>ведение кадрового учёта и обучения сотрудников;</li>`,
    `<li>информирование Субъекта ПД о статусе рассмотрения его обращений;</li>`,
    `<li>выполнение требований законодательства Российской Федерации;</li>`,
    `<li>улучшение качества предоставляемых услуг.</li>`,
    `</ul>`,

    `<h2>4. Правовые основания обработки</h2>`,
    `<p>Правовыми основаниями обработки персональных данных являются:</p>`,
    `<ul>`,
    `<li>согласие Субъекта ПД на обработку его персональных данных;</li>`,
    `<li>заключение и исполнение договора, стороной которого является Субъект ПД;</li>`,
    `<li>требования действующего законодательства Российской Федерации, в том числе Трудового кодекса РФ и Федерального закона № 152-ФЗ;</li>`,
    `<li>осуществление прав и законных интересов Оператора, не противоречащих интересам Субъекта ПД.</li>`,
    `</ul>`,

    `<h2>5. Способы и сроки обработки</h2>`,
    `<p>Обработка персональных данных осуществляется как с использованием средств автоматизации, так и без их использования. Оператор обеспечивает безопасность персональных данных при их обработке путём применения правовых, организационных и технических мер защиты.</p>`,
    `<p>Сроки обработки определяются целью обработки и требованиями законодательства. Персональные данные соискателей хранятся не более 3 (трёх) лет с момента их получения, если иное не установлено законом или согласием Субъекта ПД. Персональные данные сотрудников хранятся в течение срока, установленного трудовым законодательством.</p>`,
    `<p>По достижении целей обработки или при отзыве согласия персональные данные подлежат уничтожению, если иной срок их хранения не установлен законодательством.</p>`,

    `<h2>6. Передача персональных данных третьим лицам</h2>`,
    `<p>Оператор не передаёт персональные данные третьим лицам, за исключением случаев:</p>`,
    `<ul>`,
    `<li>наличия прямого согласия Субъекта ПД;</li>`,
    `<li>передачи в уполномоченные государственные органы по основаниям и в порядке, установленным законодательством Российской Федерации;</li>`,
    `<li>передачи лицам, привлечённым Оператором для обработки данных на основании поручения, при условии соблюдения такими лицами требований к защите персональных данных.</li>`,
    `</ul>`,
    `<p>Трансграничная передача персональных данных не осуществляется.</p>`,

    `<h2>7. Меры защиты персональных данных</h2>`,
    `<p>Для защиты персональных данных Оператор применяет:</p>`,
    `<ul>`,
    `<li>организационные меры (назначение лиц, ответственных за обработку ПД; локальные нормативные акты);</li>`,
    `<li>технические меры (шифрование каналов связи, разграничение прав доступа, резервное копирование, защита от несанкционированного доступа);</li>`,
    `<li>контроль доступа к материальным носителям информации;</li>`,
    `<li>регулярный пересмотр применяемых мер на соответствие актуальным угрозам.</li>`,
    `</ul>`,

    `<h2>8. Права Субъектов персональных данных</h2>`,
    `<p>Субъект ПД имеет право:</p>`,
    `<ul>`,
    `<li>получать информацию об обработке своих персональных данных;</li>`,
    `<li>требовать уточнения, блокирования или уничтожения своих персональных данных, если они являются неполными, устаревшими, неточными, незаконно полученными или не являются необходимыми для заявленной цели обработки;</li>`,
    `<li>отозвать своё согласие на обработку персональных данных в любой момент;</li>`,
    `<li>обжаловать действия или бездействие Оператора в уполномоченный орган по защите прав субъектов персональных данных или в судебном порядке;</li>`,
    `<li>защищать свои права и законные интересы любыми не запрещёнными законом способами.</li>`,
    `</ul>`,

    `<h2>9. Использование cookie-файлов</h2>`,
    `<p>Сайт Оператора использует cookie-файлы и аналогичные технологии для обеспечения работоспособности сервиса, аутентификации пользователей и сбора обезличенной статистики посещений. Субъект ПД может отключить cookie в настройках своего браузера, однако в этом случае часть функциональности может стать недоступной.</p>`,

    `<h2>10. Контактная информация Оператора</h2>`,
    `<p>Запросы, связанные с обработкой персональных данных (включая отзыв согласия, требования об уточнении или удалении данных), направляются по электронной почте: <a href="mailto:${contactEmail}">${contactEmail}</a>.</p>`,
    `<p>Срок ответа Оператора на обращение Субъекта ПД — не более 30 (тридцати) календарных дней с момента получения обращения.</p>`,

    `<h2>11. Изменения Политики</h2>`,
    `<p>Оператор вправе вносить изменения в настоящую Политику. Актуальная редакция Политики публикуется на сайте Оператора. Существенные изменения доводятся до сведения Субъектов ПД дополнительно.</p>`,

    `<p><em>Дата вступления в силу настоящей редакции: ${today}</em></p>`,
  ].join("\n")
}
