/** Подобрать примеры вакансий по нише */
export function getExamplesForNiche(category: string): VacancyExample[] {
  const lower = category.toLowerCase()
  // Попытка точного совпадения
  const exact = VACANCY_EXAMPLES.filter(e => lower.includes(e.niche.toLowerCase()))
  if (exact.length > 0) return exact
  // Иначе отдаём все примеры
  return VACANCY_EXAMPLES
}
