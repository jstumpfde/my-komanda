export const TEMPLATE_VARIABLES = DEMO_PICKER_VARIABLES

export type TemplateVariableKey = typeof TEMPLATE_VARIABLES[number]["key"]

export function getVariableExamples(): Record<string, string> {
  return Object.fromEntries(TEMPLATE_VARIABLES.map(v => [v.key, v.example]))
}

// ─── Helpers ──────────────────────────────────────────────────────────────

function tb(id: string, content: string): Block {
  return { ...createBlock("text"), id, content }
}

function infoBlock(id: string, content: string, style: "info" | "warning" | "success" | "error" = "info"): Block {
  return { ...createBlock("info"), id, content, infoStyle: style }
}

function taskBlock(id: string, desc: string, questions: Question[]): Block {
  return { ...createBlock("task"), id, taskDescription: desc, questions }
}

function q(id: string, text: string, answerType: Question["answerType"] = "short", options: string[] = []): Question {
  return { ...defaultQuestion(), id, text, answerType, options }
}

function lesson(id: string, emoji: string, title: string, blocks: Block[]): Lesson {
  return { id, emoji, title, blocks }
}

