async function upsertLayer(layer: LayerSeed, force: boolean) {
  if (!force) {
    // По умолчанию — insert-only: не затираем то, что HR/владелец мог уже
    // отредактировать в админке (app/(admin)/admin/tip/prompt-layers-tab.tsx).
    const inserted = await db
      .insert(tipPromptLayers)
      .values({
        layerKey: layer.layerKey,
        title: layer.title,
        content: layer.content,
        isActive: true,
      })
      .onConflictDoNothing({ target: tipPromptLayers.layerKey })
      .returning({ layerKey: tipPromptLayers.layerKey })
    if (inserted.length > 0) {
      console.log(`  ok: ${layer.layerKey}`)
    } else {
      console.log(`  skip (существует): ${layer.layerKey}`)
    }
    return
  }

  // --force — полная перезапись контента до актуального состояния файла
  // (осознанный откат ручных правок в админке, использовать с осторожностью).
  await db
    .insert(tipPromptLayers)
    .values({
      layerKey: layer.layerKey,
      title: layer.title,
      content: layer.content,
      isActive: true,
    })
    .onConflictDoUpdate({
      target: tipPromptLayers.layerKey,
      set: {
        title: layer.title,
        content: layer.content,
        updatedAt: new Date(),
      },
    })
  console.log(`  ok (force): ${layer.layerKey}`)
}

