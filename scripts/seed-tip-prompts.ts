const SHADES_CONTENT = `Дополнительное правило: учитывать исходные цифры
При расчёте каждой позиции нужно учитывать не только итоговую цифру, но и исходные цифры, из которых она получилась.

Например:
24 → 2+4 = 6
В этом случае основная цифра дня — 6, но внутри неё есть оттенки 2 и 4.
Это значит:
- 6 — главная энергия дня: ответственность, забота, качество, сервис, семья, гармония.
- 2 — дополнительный оттенок: чувствительность, дипломатия, партнёрство, зависимость от атмосферы.
- 4 — дополнительный оттенок: порядок, структура, практичность, надёжность, трудолюбие.

Поэтому день 24 нужно трактовать не просто как 6, а как:
«Ответственная и заботливая энергия 6, усиленная чувствительностью 2 и практичностью 4».

Как использовать оттенки
- Итоговая цифра всегда главная.
- Исходные цифры показывают, каким способом эта энергия проявляется.
- Если исходная цифра повторяется, её влияние усиливается.
- Если в дате много одинаковых цифр, это нужно отдельно отметить.
- Если какой-то цифры в дате совсем нет, не нужно делать жёсткий вывод, что качества отсутствуют. Лучше писать мягко: «эта энергия может быть менее естественной и её важно развивать через опыт».

Пример
Дата: 24.11.1998
День: 24 → 2+4 = 6
Основная цифра дня: 6. Оттенки дня: 2 и 4.
Трактовка:
День 24 даёт человеку ответственность, заботу, стремление к качеству и желание быть полезным. Но эта 6 проявляется не сама по себе, а через сочетание 2 и 4.
От 2 здесь появляется чувствительность, умение чувствовать людей, важность партнёрства и атмосферы.
От 4 появляется практичность, желание порядка, надёжность, стремление делать правильно и основательно.
В плюсе это человек, который может заботиться не хаотично, а практично: создавать порядок, помогать делом, выстраивать комфорт, быть надёжным.
В минусе может появляться обидчивость 2, жёсткость 4 и спасательство 6. Человек может ждать благодарности, переживать из-за отношения других и одновременно пытаться контролировать ситуацию через заботу.`

