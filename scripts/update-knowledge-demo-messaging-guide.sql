<h2>Антипаттерны — что убивает конверсию</h2>
<ul>
<li><strong>Длинные простыни текста.</strong> Кандидат теряет нить до того, как доходит до ссылки. 3–5 предложений — потолок для одного сообщения.</li>
<li><strong>Канцелярит.</strong> «В соответствии с...», «просим сообщить», «настоящим уведомляем» — звучит как казённая проверка, а не как приглашение от живого человека.</li>
<li><strong>Рамка экзамена.</strong> «Пройдите обязательную проверку» включает защитную реакцию. «Посмотрите, как устроена работа» — та же по сути ссылка, но подана как любопытство, а не как тест.</li>
<li><strong>Без обещания объёма.</strong> «Заполните анкету» без «это займёт 5 минут» — неизвестность пугает сильнее, чем сам объём работы.</li>
<li><strong>Без имени.</strong> Обезличенное «Здравствуйте!» не создаёт ощущения личного отбора — {{name}} должно быть в первой же фразе.</li>
</ul>

<h2>Демо: сколько блоков и в каком порядке</h2>
<p>На платформе три готовых шаблона демо — выбирать по роли, а не по желанию рассказать «всё»:</p>
<table>
<thead><tr><th>Шаблон</th><th>Блоков</th><th>Время</th><th>Для каких ролей</th></tr></thead>
<tbody>
<tr><td>Короткая</td><td>6–7</td><td>5–7 мин</td><td>Массовые позиции — курьер, продавец, оператор, администратор</td></tr>
<tr><td>Средняя</td><td>10–11</td><td>10–12 мин</td><td>Специалисты — МОП, младший маркетолог, HR, координатор</td></tr>
<tr><td>Полная</td><td>14–16</td><td>15–20 мин</td><td>Ключевые и руководящие роли — маркетолог, РОП, директор направления</td></tr>
</tbody>
</table>
<p><strong>Порядок:</strong> заголовок и один яркий факт → ценность (деньги, формат, задачи) в первых 2–3 блоках → детали и условия → «о компании» — в середину или конец, не в начало → форма контактов — последним блоком.</p>
<p><strong>Обещание объёма на первом экране</strong> платформа считает и показывает сама — «≈N минут · M шагов» — автоматически, по фактическому числу и типу блоков. Отдельно настраивать не нужно: чем короче и легче блоки, тем честнее и привлекательнее цифра, которую увидит кандидат.</p>
<p>Середину демо сокращать не нужно — данные показывают, что дошедшие до 5-го блока заканчивают демо почти всегда. Урезать стоит именно первый экран и вход, а не содержание.</p>

