<h2>Почему сообщение решает всё</h2>
<p>Кандидат решает, открывать демо или нет, ещё до того, как увидит хоть один блок контента. По разрезу прода (531 кандидат, одна вакансия, 05.07.2026): <strong>77–85% из тех, кто бросил демо, бросают «с порога»</strong> — не пройдя ни одного из 16 блоков. А те, кто дошёл до 5-го блока, <strong>заканчивают демо практически всегда</strong>. Содержание демо почти ни при чём — решает вход: текст приглашения и первый экран.</p>
<p>Рабочая формула, проверенная на практике: <em>«Поздравляем, вы нам подходите, заполните ещё вот эту анкету»</em>. Кандидаты заполняли анкету с удовольствием — потому что их уже «выбрали», а не просят пройти проверку.</p>

